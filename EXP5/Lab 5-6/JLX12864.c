//使能GPIO
void LCD_PORT_init(void)
{
    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOC);    //使能PORTC
    while(!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOC));
    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOK);    //使能PORTK
    while(!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOK));
    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOB);    //使能PORTB
    while(!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOB));
    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOE);    //使能PORTE
    while(!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOE));

    GPIOPinTypeGPIOOutput(GPIO_PORTK_BASE, GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_2);
    GPIOPinTypeGPIOOutput(GPIO_PORTC_BASE, GPIO_PIN_5 | GPIO_PIN_4 );
    GPIOPinTypeGPIOOutput(GPIO_PORTB_BASE, GPIO_PIN_4 | GPIO_PIN_5 );
    GPIOPinTypeGPIOOutput(GPIO_PORTE_BASE, GPIO_PIN_4 );
    GPIOPinTypeGPIOInput(GPIO_PORTC_BASE, GPIO_PIN_6);

    SysCtlPeripheralEnable(SYSCTL_PERIPH_GPIOF);    //LCD_delay时间测试
    while(!SysCtlPeripheralReady(SYSCTL_PERIPH_GPIOF));	//LCD_delay时间测试
    GPIOPinTypeGPIOOutput(GPIO_PORTF_BASE, GPIO_PIN_1 );	//LCD_delay时间测试
}

//延时
void LCD_delay(int n_ms)
{
    int j,k;
    TEST_L;
    for(j=0; j<n_ms; j++)
        for(k=0; k<110; k++);
    TEST_H;
}

//短延时
void LCD_delay_us(int n_us)
{
    int j,k;
    TEST_L;
    for(j=0; j<n_us; j++)
        for(k=0; k<1; k++);
    TEST_H;
}



//写指令到 LCD 模块
void transfer_command_lcd(int data1)
{
    char i;
    LCD_CS_L;
    LCD_RS_L;
    for(i=0; i<8; i++)
    {
        LCD_SCK_L;
        //LCD_delay_us(10); //加少量延时
        if(data1&0x80) LCD_SDA_H;
        else LCD_SDA_L;
        LCD_SCK_H;
        //LCD_delay_us(10); //加少量延时
        data1=data1<<=1;
    }
    LCD_CS_H;
}

//写数据到 LCD 模块
void transfer_data_lcd(int data1)
{
    char i;
    LCD_CS_L;
    LCD_RS_H;
    for(i=0; i<8; i++)
    {
        LCD_SCK_L;
        if(data1&0x80) LCD_SDA_H;
        else LCD_SDA_L;
        LCD_SCK_H;
        data1=data1<<=1;
    }
    LCD_CS_H;
}

//LCD 模块初始化
void initial_lcd()
{
    LCD_PORT_init();
    LCD_RST_L; //低电平复位
    LCD_delay(100);
    LCD_RST_H; //复位完毕
    LCD_delay(100);
    transfer_command_lcd(0xe2); //软复位
    LCD_delay(5);
    transfer_command_lcd(0x2c); //升压步聚 1
    LCD_delay(50);
    transfer_command_lcd(0x2e); //升压步聚 2
    LCD_delay(50);
    transfer_command_lcd(0x2f); //升压步聚 3
    LCD_delay(5);
    transfer_command_lcd(0x23); //粗调对比度，可设置范围 0x20～0x27
    transfer_command_lcd(0x81); //微调对比度
    transfer_command_lcd(0x28); //微调对比度的值，可设置范围 0x00～0x3f
    transfer_command_lcd(0xa2); //1/9 偏压比（bias）
    transfer_command_lcd(0xc8); //行扫描顺序：从上到下
    transfer_command_lcd(0xa0); //列扫描顺序：从左到右
    transfer_command_lcd(0x40); //起始行：第一行开始
    transfer_command_lcd(0xaf); //开显示
}

void lcd_address(uint32_t page,uint32_t column)
{
    column=column-0x01;
    transfer_command_lcd(0xb0+page-1); //设置页地址，每 8 行为一页，全屏共 64 行，被分成 8 页
    transfer_command_lcd(0x10+(column>>4&0x0f)); //设置列地址的高 4 位
    transfer_command_lcd(column&0x0f); //设置列地址的低 4 位
}

//全屏清屏
void clear_screen()
{
    unsigned char i,j;

    for(i=0; i<9; i++)
    {
        transfer_command_lcd(0xb0+i);
        transfer_command_lcd(0x10);
        transfer_command_lcd(0x00);
        for(j=0; j<132; j++)
        {
            transfer_data_lcd(0x00);
        }
    }
}

//显示 128x64 点阵图像
void display_128x64(uint8_t *dp)
{
    uint32_t i,j;

    for(j=0; j<8; j++)
    {
        lcd_address(j+1,1);
        for (i=0; i<128; i++)
        {
            transfer_data_lcd(*dp); //写数据到 LCD,每写完一个 8 位的数据后列地址自动加 1
            dp++;
        }
    }
}

